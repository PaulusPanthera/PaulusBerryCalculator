function getYieldProfile(yieldPerPlot) {
  const profiles = new Map([
    [4.5, [3, 4, 5, 6]],
    [5.5, [4, 5, 6, 7]],
    [6.0, [5, 6, 7]],
    [6.5, [5, 6, 7, 8]],
    [8.0, [7, 8, 9]],
    [8.5, [7, 8, 9, 10]],
    [11.5, [10, 11, 12, 13]],
    [12.0, [11, 12, 13]],
  ]);

  return profiles.get(Number(yieldPerPlot)) ?? [];
}

